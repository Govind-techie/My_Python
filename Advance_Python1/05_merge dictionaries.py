# Merge Dictionary : merge dictionary operator (|) is used to merge two dictionary stored in 2 different variable and stored in form of dict.

dict1 = {"num1":1,"num2":2} # Dict_1
dict2 = {"num3":3,"num4":4} # Dict_2

merged_dict = dict1 | dict2 # Here, we created a separate variable to store the merged dictionary using (|) operator.
print(merged_dict) # Here, we printed the merged dictionary.